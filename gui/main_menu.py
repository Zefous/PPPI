class MainMenu:
    """Класс для отображения и управления главным меню игры."""

    def __init__(self):
        """Инициализация элементов главного меню."""
        self.options = [
            "Новая игра",
            "Загрузить сохранение",
            "Галерея",
            "Обучение",
            "Настройки"
        ]

    def display(self):
        """Отображает главное меню с доступными опциями."""
        print("Главное меню:")
        for option in self.options:
            print(f"- {option}")